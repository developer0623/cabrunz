package com.cabrunzltd.user.models;

public class Walkerinfo {

	private String id;
	private String distance;
	private String latitude;
	private String longitude;
	private String type;
	private String base_price;
	private String distance_cost;
	private String time_cost;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDistance() {
		return distance;
	}

	public void setDistance(String distance) {
		this.distance = distance;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getBase_price() {
		return base_price;
	}

	public void setBase_price(String base_price) {
		this.base_price = base_price;
	}

	public String getDistance_cost() {
		return distance_cost;
	}

	public void setDistance_cost(String distance_cost) {
		this.distance_cost = distance_cost;
	}

	public String getTime_cost() {
		return time_cost;
	}

	public void setTime_cost(String time_cost) {
		this.time_cost = time_cost;
	}

}
